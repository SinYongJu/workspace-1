/* 자바 프로글래밍 - 프로그램을 실행시킬 때 시작점*/
public class Test06 {
  public static void main(String[] args){
    System.out.println("Hello");
  }
}
/*
  엔트리 포인트 (entry point)
  -프로그램을 실행시킬 때 가장 처음 실행하는 부분을 가리킨다.

  main 메서드(=함수)
  -함수 중에서 객체의 값을 다루는 함수를 보통 '메서드(method)'라 부른다.
  -자바에서는 모든 함수가 클래스 블록안에 선언되기 때문에,
  함수(function)라는 용어보다 메소드(method) 라는 용어를 사용한다.

  main 메서드
  - 클래스 블록을 실행할 때 가장 먼저 실행하는 코드 블록이다.
  - main() 메서드가 없으면 해당 클래스를 Jvm이 실행 할 수 없다.
*/
